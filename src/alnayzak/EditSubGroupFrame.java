    public EditSubGroupFrame(Pgroup subgroup) {
        initComponents();
         emf=Persistence.createEntityManagerFactory("AlNayzakPU");
        em=emf.createEntityManager();
        this.NumberTextField.setText(subgroup.getGroupnumber()+"");
        this.SubGroupNameTextField.setText(subgroup.getGroupname()+"");
        group= new Pgroup();
        qr=em.createNamedQuery("Pgroup.findAll");
        groups=qr.getResultList();
        generalGroups=new ArrayList<Pgroup>();
        for (Pgroup x :groups){
            if(x.getSubgroupnumber()==null){
                generalGroups.add(x);
                this.GeneralGroupComboBox.addItem(x.getGroupname());
            }
        }
    }

