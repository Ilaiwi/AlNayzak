    public void initialData(){
        
        qr=em.createNamedQuery("Employee.findAll");
        employee= qr.getResultList();
        for(int i=0; i<employee.size();i++){
            this.ProjectLeaderComboBox.addItem(employee.get(i).getPeople().getFname()+" "+employee.get(i).getPeople().getMinname()+" "+employee.get(i).getPeople().getLname());  
        }
        qr=em.createNamedQuery("Department.findAll");
        department=qr.getResultList();
        for (Department x:department){
            this.DepartmentComboBox.addItem(x.getDepartnentname());
        }
    }
    
    public EditResearchandDevelopmentProjectFrame(Dprojects dproject) {
        initComponents();
        emf=Persistence.createEntityManagerFactory("AlNayzakPU");
        em=emf.createEntityManager();
        qr=em.createNamedQuery("Dprojects.findByDprojectnumber");
        qr.setParameter("dprojectnumber", dproject.getDprojectnumber());
        dprojects=(Dprojects) qr.getSingleResult();
        initialData();
        this.ProjectNameTextField.setText(dproject.getDprojectname());
        this.ProjectNumberTextField.setText(dproject.getDprojectnumber()+"");
        this.ProjectLeaderComboBox.setSelectedItem(dproject.getManagernumber());
        this.ProjectNumberTextField.disable();
    }

