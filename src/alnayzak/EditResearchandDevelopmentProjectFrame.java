    private void EditDevelopmentProjectButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EditDevelopmentProjectButtonActionPerformed
        try{
            dprojects.setDprojectnumber(BigDecimal.valueOf(Double.valueOf(this.ProjectNumberTextField.getText())));
        dprojects.setDprojectname(this.ProjectNameTextField.getText());
        dprojects.setStartdate(new Date(Integer.valueOf((String)this.YearComboBox.getSelectedItem()),Integer.valueOf((String)this.MonthComboBox.getSelectedItem()),Integer.valueOf((String)this.DayComboBox.getSelectedItem())));
        dprojects.setManagernumber(employee.get(this.ProjectLeaderComboBox.getSelectedIndex()));
        dprojects.setDepartmentnumber(department.get(this.DepartmentComboBox.getSelectedIndex()));
        em.getTransaction().begin();
        em.getTransaction().commit();
        JOptionPane.showMessageDialog(null,"Done");
        this.ProjectLeaderComboBox.setSelectedIndex(0);
        this.ProjectNameTextField.setText("");
        this.ProjectNumberTextField.setText("");
        this.DepartmentComboBox.setSelectedIndex(0);
        this.dispose();
         }
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(null, "Check Your Enteries");
        }
    }//GEN-LAST:event_EditDevelopmentProjectButtonActionPerformed

