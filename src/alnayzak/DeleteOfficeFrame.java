    private void DeleteOfficeButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteOfficeButtonActionPerformed
        try{
            Office selectedoffice = offices.get(this.NameComboBox.getSelectedIndex());
        BigDecimal officenumber = selectedoffice.getOfficenumber();
        em.getTransaction().begin();
        office=em.find(Office.class, officenumber);
        em.remove(office);
        em.getTransaction().commit();
        JOptionPane.showMessageDialog(null,"Done");
        this.NameComboBox.removeAllItems();;
        initialData();
         }
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(null, "Check Your Enteries");
        }
    }//GEN-LAST:event_DeleteOfficeButtonActionPerformed

